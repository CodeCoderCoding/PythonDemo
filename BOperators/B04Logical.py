def logical():
    # OR Expression
    my_bool = True or False
    print(my_bool)

    # AND Expression
    my_bool = True and False
    print(my_bool)

    # NOT expression
    my_bool = False
    print(not my_bool)


def bit_value():
    print(10 * True)
    print(10 * False)


# Press the green button in the gutter to run the script.
if __name__ == '__main__':
    logical()
    bit_value()
